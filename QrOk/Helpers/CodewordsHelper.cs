    public static int GetTotalNumberOfCodewords(ErrorCorrectionLevel level, int version)
    {
        var group = _codewordsInfo[(int)level][version - 1];
        return group[0][0] * group[0][1] + group[1][0] * group[1][1];
    }

    public static (int, int, int, int) GetCodewordsGroupInfo(ErrorCorrectionLevel level, int version)
    {
        var group = _codewordsInfo[(int)level][version - 1];
        return (group[0][0], group[0][1], group[1][0], group[1][1]);
    }

    public static int GetECNumber(ErrorCorrectionLevel level, int version)
    {
        var group = _codewordsInfo[(int)level][version - 1];
        return group[2][0];
    }
