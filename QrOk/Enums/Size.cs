namespace QrOk.Enums;

/// <summary>
/// Defines a set of predefined sizes.
/// </summary>
public enum Size
{
    /// <summary>
    /// The original, unmodified size.
    /// </summary>
    Original = 1,

    /// <summary>
    /// Extra small size, up to 200x200 pixels.
    /// </summary>
    XS = 200,

    /// <summary>
    /// Small size, up to 400x400 pixels.
    /// </summary>
    S = 400,

    /// <summary>
    /// Medium size, up to 600x600 pixels.
    /// </summary>
    M = 600,

    /// <summary>
    /// Large size, up to 800x800 pixels.
    /// </summary>
    L = 800,

    /// <summary>
    /// Extra large size, up to 1000x1000 pixels.
    /// </summary>
    XL = 1000
}